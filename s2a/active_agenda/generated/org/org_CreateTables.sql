CREATE TABLE `org` (
   OrganizationID int unsigned auto_increment not null,
   ParentOrganizationID int unsigned null,
   DirectionOrganizationID int unsigned null,
   PolicyOrganizationID int unsigned null,
   Name varchar(128),
   DBA varchar(128),
   ShortName varchar(20),
   Description text,
   Mission text,
   SIC varchar(20),
   URL varchar(128),
   Address1 text,
   Address2 varchar(128),
   CountryID int unsigned,
   StateID int unsigned,
   CountyID int unsigned,
   City varchar(50),
   PostalCode varchar(50),
   Phone varchar(50),
   Fax varchar(50),
   Email varchar(128),
   Participant bool,
   Internal bool default 0,
   Public bool default 0,
   Customer bool default 0,
   Contractor bool default 0,
   Manufacturer bool default 0,
   Supplier bool default 0,
   InsuranceBroker bool default 0,
   InsuranceCarrier bool default 0,
   Reinsurer bool default 0,
   MedicalProvider bool default 0,
   LawFirm bool default 0,
   StandardsProvider bool default 0,
   AccreditingBody bool default 0,
   GovAgency bool default 0,
   DisposalFacility bool default 0,
   WasteTransporter bool default 0,
   AgencyLevelID int unsigned,
   _ModDate datetime not null,
   _ModBy int unsigned not null default 0,
   _Deleted bool not null default 0,
   _TransactionID bigint unsigned not null default 0,
   PRIMARY KEY(
      OrganizationID
   )
) TYPE=InnoDB;

